    public class XmlDocumentCommentBuilder
    {
        public XmlDocumentCommentBuilder(SourceBuilder sourceBuilder)
        {
            SourceBuilder = sourceBuilder;
        }

        #region property

        private SourceBuilder SourceBuilder { get; }

        #endregion

        #region function

        public virtual string ToDocumentComment(string s)
        {
            return SourceBuilder.EditLine(s, "/// ", string.Empty);
        }

        /// <summary>
        /// 行一覧を XML ドキュメントコメント形式に変換する。
        /// </summary>
        /// <param name="lines"></param>
        /// <returns></returns>
        public virtual IEnumerable<string> ToDocumentComments(IEnumerable<string> lines)
        {
            return lines.Select(a => ToDocumentComment(a));
        }

        /// <summary>
        /// 文字列を XML ドキュメントコメント形式に変換する。
        /// </summary>
        /// <param name="xml"></param>
        /// <returns></returns>
        public IEnumerable<string> ToDocumentComments(string xml)
        {
            return ToDocumentComments(SourceBuilder.SplitLines(xml));
        }

        #endregion
    }
